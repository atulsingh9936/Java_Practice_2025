package Java_practice.JAVA_07_04_2025;

public class Left_triangle {
    public static void main(String[] args) {
        int n =5;
        for(int i=1;i<=n;i++){
            for(int j=i;j<=n;j++){
                System.out.print("*"+" ");
            }
            System.out.println();

        }
    }
}
